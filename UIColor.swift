import UIKit

extension UIColor {
    
    public class func appleRed() -> UIColor! {
        return UIColor(red: 255/255.0, green: 45/255.0, blue: 85/255.0, alpha: 1.0)
    }
    
    public class func uberWhite() -> UIColor! {
        return UIColor(red: 249/255.0, green: 249/255.0, blue: 249/255.0, alpha: 1.0)
    }
    
    public class func seaShell() -> UIColor! {
        return UIColor(red: 205/255.0, green: 197/255.0, blue: 191/255.0, alpha: 1.0)
    }
}
